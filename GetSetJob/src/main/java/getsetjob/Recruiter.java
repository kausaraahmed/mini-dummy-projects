    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            int index = jTable1.getSelectedRow();
            String p0st, typ3, sk1ll, empTyp3 = "", vacancy, d3adline;
            p0st = jTable1.getValueAt(index, 0).toString();
            typ3 = jTable1.getValueAt(index, 1).toString();
            sk1ll = jTable1.getValueAt(index, 2).toString();
            empTyp3 = jTable1.getValueAt(index, 3).toString();
            d3adline = jTable1.getValueAt(index, 4).toString();
            vacancy = jTable1.getValueAt(index, 5).toString(); // Corrected index for the vacancy field
            jobPostField.setText(p0st);
            typeComboBox.setSelectedItem(typ3);
            skillComboBox.setSelectedItem(sk1ll);

            // Clearing selection before setting the radio buttons
            buttonGroup1.clearSelection();

            if (empTyp3.equals("Full Time")) {
                fullTimeRadioButton.setSelected(true);
            } else if (empTyp3.equals("Part Time")) {
                partTimeRadioButton.setSelected(true);
            } else if (empTyp3.equals("Intern")) {
                internRadioButton.setSelected(true);
            }

            vacancyField.setText(vacancy);
            Date d = new SimpleDateFormat("MMM d, y").parse((String) d3adline);
            dateChooser.setDate(d);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTable1MouseClicked

