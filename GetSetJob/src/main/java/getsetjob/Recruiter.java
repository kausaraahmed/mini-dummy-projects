    private void postButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_postButtonActionPerformed
        String p0st, typ3, sk1ll, empTyp3 = "", vacancy, d3adline;

        p0st = jobPostField.getText();
        typ3 = typeComboBox.getSelectedItem().toString();
        sk1ll = skillComboBox.getSelectedItem().toString();

        if (fullTimeRadioButton.isSelected()) {
            empTyp3 = "Full Time";
        }
        if (partTimeRadioButton.isSelected()) {
            empTyp3 = "Part Time";
        }
        if (internRadioButton.isSelected()) {
            empTyp3 = "Intern";
        }
        vacancy = vacancyField.getText();
        d3adline = ((JTextField) dateChooser.getDateEditor().getUiComponent()).getText();

        if (p0st.equals("")) {
            JOptionPane.showMessageDialog(null, "Enter post Title", "Insertion error", 0);
        } else if (typeComboBox.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Select job type", "Insertion error", 0);
        } else if (skillComboBox.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Select skills", "Insertion error", 0);
        } else if (empTyp3.equals("")) {
            JOptionPane.showMessageDialog(null, "Enter employee type", "Insertion error", 0);
        } else if (vacancy.equals("")) {
            JOptionPane.showMessageDialog(null, "Enter vacancy", "Insertion error", 0);
        } else if (d3adline.equals("")) {
            JOptionPane.showMessageDialog(null, "Enter deadline", "Insertion error", 0);
        } else {
            Job j = new Job(p0st, typ3, sk1ll, empTyp3, d3adline, Integer.parseInt(vacancy));
            jobList.add(j);

            dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            Object row[] = new Object[8];
            for (Job x : jobList) {
                row[0] = x.post;
                row[1] = x.type;
                row[2] = x.skill;
                row[3] = x.empType;
                row[4] = x.deadline;
                row[5] = x.vacancy;
                dtm.addRow(row);
            }
            jobPostField.setText("");
            typeComboBox.setSelectedIndex(0);
            skillComboBox.setSelectedIndex(0);
            buttonGroup1.clearSelection();
            vacancyField.setText("");
            dateChooser.setCalendar(null);
        }
    }//GEN-LAST:event_postButtonActionPerformed

    private void exitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exitButtonActionPerformed
        int p = JOptionPane.showConfirmDialog(this, "Are you want to exit?", "Confirmation", 0, 2);
        if (p == 0) {
            try {
                FileWriter fw = new FileWriter("jobList.txt", true);
                BufferedWriter bw = new BufferedWriter(fw);

                for (Job x : jobList) {
                    bw.write(x.post + " " + x.type + " " + x.skill + " " + x.empType + " " + x.deadline + " " + x.vacancy);
                    bw.newLine();
                }
                bw.close();
            } catch (IOException ex) {
            }
            System.exit(0);
        }
    }//GEN-LAST:event_exitButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        dtm = (DefaultTableModel) jTable1.getModel();
        if (jTable1.getSelectedRowCount() == 1) {
            int index = jTable1.getSelectedRow();
            int p = JOptionPane.showConfirmDialog(this, "Are you want to DELETE this post?", "Confirm deletion", 0, 2);
            if (p == 0) {
                dtm.removeRow(index);
                jobList.remove(index);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Select only one job", "Multiple jobs selected", 2);
        }
        jobPostField.setText("");
        typeComboBox.setSelectedIndex(0);
        skillComboBox.setSelectedIndex(0);
        buttonGroup1.clearSelection();
        vacancyField.setText("");
        dateChooser.setCalendar(null);
    }//GEN-LAST:event_deleteButtonActionPerformed

